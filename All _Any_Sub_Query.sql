--Q1 .WAQTD NAME OF THE EMPLOYEES EARNING SALARY MORE THAN THE SALESMAN. 
SELECT ENAME
FROM SCOTT.EMP
WHERE SAL >ALL(SELECT SAL
               FROM SCOTT.EMP
               WHERE JOB='SALESMAN');

-- Q2 WAQTD DETAILS OF THE EMPLOYEES HIRED AFTER ALL THE  CLERKS. 
SELECT *
FROM SCOTT.EMP
WHERE  HIREDATE >ALL(SELECT HIREDATE
                     FROM SCOTT.EMP
                     WHERE JOB='CLARK');  

 -- Q3 .WAQTD NAME AND SALARY FOR ALL THE EMPLOYEES IF THEY  ARE EARNING LESS THAN ATLEST A MANAGER   
 SELECT ENAME,SAL
 FROM SCOTT.EMP
 WHERE SAL<(SELECT MAX(SAL)
            FROM SCOTT.EMP
            WHERE SAL IN(SELECT SAL
                         FROM SCOTT.EMP
                         WHERE JOB='MANAGER')
            );  
  --  OR    
