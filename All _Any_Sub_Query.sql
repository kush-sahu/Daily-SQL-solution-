 -- Q7.WAQTD DETAILS OF EMPLOYEES WORKING IN ACCOUNTING OR SALES DEPT.                     
SELECT *
FROM SCOTT.EMP
WHERE DEPTNO IN(SELECT DEPTNO
                FROM SCOTT.DEPT
                WHERE DNAME IN('ACCOUNTING','SALES'));

 -- Q8  .WAQTD DEPARTMENT NAMES OF THE EMPOYEES WITH NAME SMITH , KING AND MILLER.               
SELECT DNAME
FROM SCOTT.DEPT
WHERE DEPTNO IN(SELECT DEPTNO
                FROM SCOTT.EMP
                WHERE ENAME IN('SMITH','KING','MILLER'));


